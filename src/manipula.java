public class manipula {
    public static void main(String[] args) {
        double num = 5;

        num += 2;
        System.out.println("Después de +=2: "+ num);
        num -= 3;
        System.out.println("Después de -=3: "+ num);
        num *= 4;
        System.out.println("Después de *=4: "+ num);
        num /= 2;
        System.out.println("Después de /=2: "+ num);
        num %= 5;
        System.out.println("Después de %=5: "+ num);
    }
}
